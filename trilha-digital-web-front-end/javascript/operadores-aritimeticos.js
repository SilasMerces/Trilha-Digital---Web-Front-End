// Operadores Aritméticos

let primeiroNome = 20
let segundoNome = 2

// Adição:   +
// Subtração:   -
// Multiplicação:   *
// Divisão:   /
// Exponenciação:   **
// Resto da divisão:   %


console.log('Soma =', primeiroNome + segundoNome)
console.log('Subtração =', primeiroNome - segundoNome)
console.log('Multiplicação =', primeiroNome * segundoNome)
console.log('Divisão =', primeiroNome / segundoNome)
console.log('Exponenciação =', primeiroNome ** segundoNome)
console.log('Resto da divisão =', primeiroNome % segundoNome)


